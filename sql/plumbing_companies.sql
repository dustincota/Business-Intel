create table if not exists public.plumbing_companies (
    company_id bigint generated by default as identity primary key,
    name text not null,
    brand text,
    plumbing_type text not null default 'general',
    address text,
    city text,
    state text,
    zipcode text,
    county text,
    latitude double precision,
    longitude double precision,
    phone text,
    website text,
    google_place_id text not null,
    google_rating numeric(3, 2),
    google_review_count integer,
    business_status text,
    primary_place_type text,
    place_types jsonb,
    source_state text,
    source_query text,
    data_source text not null default 'google_places',
    created_at timestamptz not null default timezone('utc', now()),
    updated_at timestamptz not null default timezone('utc', now())
);

create unique index if not exists plumbing_companies_google_place_id_idx
    on public.plumbing_companies (google_place_id);

create index if not exists plumbing_companies_state_city_idx
    on public.plumbing_companies (state, city);

create index if not exists plumbing_companies_type_idx
    on public.plumbing_companies (plumbing_type);

create index if not exists plumbing_companies_brand_idx
    on public.plumbing_companies (brand);

create index if not exists plumbing_companies_source_state_idx
    on public.plumbing_companies (source_state);

create or replace function public.set_plumbing_companies_updated_at()
returns trigger
language plpgsql
as $$
begin
    new.updated_at = timezone('utc', now());
    return new;
end;
$$;

drop trigger if exists plumbing_companies_set_updated_at on public.plumbing_companies;

create trigger plumbing_companies_set_updated_at
before update on public.plumbing_companies
for each row
execute function public.set_plumbing_companies_updated_at();

grant usage on schema public to anon, authenticated, service_role;
grant select, insert, update, delete on public.plumbing_companies to anon, authenticated, service_role;
grant usage, select on sequence public.plumbing_companies_company_id_seq to anon, authenticated, service_role;
